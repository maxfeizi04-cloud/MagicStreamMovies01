const rankingTranslations = {
  excellent: { zh: "强烈推荐", en: "Excellent" },
  okay: { zh: "一般", en: "Okay" },
  bad: { zh: "较差", en: "Bad" },
  terrible: { zh: "很差", en: "Terrible" },
};

const normalizeKey = (value) => value?.toLowerCase().replace(/[^a-z-]/g, " ").trim();

export const translateGenreName = (genreName, language) => {
  const key = normalizeKey(genreName);
  return genreTranslations[key]?.[language] ?? genreName;
};

export const translateRankingName = (rankingName, language) => {
  const key = normalizeKey(rankingName);
  return rankingTranslations[key]?.[language] ?? rankingName;
};
